VCF_CONTENT = """\
    ##fileformat=VCFv4.2
    ##reference=GRCh37
    ##FILTER=<ID=PASS,Description="All filters passed">
    #CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE
    10\t96541616\trs4244285\tG\tA\t.\tPASS\t.\tGT\t0/1
    10\t114758349\trs7903146\tC\tT\t.\tPASS\t.\tGT\t0/1
    19\t44908684\trs429358\tT\tC\t.\tPASS\t.\tGT\t0/1
    11\t5246696\t.\tA\tT\t.\tPASS\t.\tGT\t1/1
"""


class TestVcfParser:
    def setup_method(self):
        self.path = _write_tmp(VCF_CONTENT, ".vcf")

    def teardown_method(self):
        os.unlink(self.path)

    def test_returns_list(self):
        assert isinstance(parse_vcf(self.path), list)

    def test_skips_meta_lines(self):
        variants = parse_vcf(self.path)
        for v in variants:
            assert not str(v.get("rsid", "")).startswith("#")
            assert not str(v.get("rsid", "")).startswith("CHROM")

    def test_rsid_extracted_from_id_column(self):
        variants = parse_vcf(self.path)
        rsids = [v["rsid"] for v in variants if v.get("rsid", "").startswith("rs")]
        assert len(rsids) >= 3

    def test_position_numeric(self):
        for v in parse_vcf(self.path):
            assert isinstance(v["position"], int)


# ─────────────────────────────────────────────────────────────────────────────
# Format detector
# ─────────────────────────────────────────────────────────────────────────────
class TestFormatDetector:
    def test_detects_23andme(self):
        path = _write_tmp(TWENTYTHREEME_CONTENT, ".txt")
        try:
            assert detect_format(path) == "23andme"
        finally:
            os.unlink(path)

    def test_detects_ancestry(self):
        path = _write_tmp(ANCESTRY_CONTENT, ".txt")
        try:
            fmt = detect_format(path)
            assert fmt in ("ancestry", "23andme")  # both .txt; header distinguishes
        finally:
            os.unlink(path)

    def test_detects_myheritage(self):
        path = _write_tmp(MYHERITAGE_CONTENT, ".csv")
        try:
            assert detect_format(path) == "myheritage"
        finally:
            os.unlink(path)

    def test_detects_vcf(self):
        path = _write_tmp(VCF_CONTENT, ".vcf")
        try:
            assert detect_format(path) == "vcf"
        finally:
            os.unlink(path)

    def test_unknown_returns_unknown(self):
        path = _write_tmp("this is not a dna file\n", ".txt")
        try:
            result = detect_format(path)
            assert result == "unknown"
        finally:
            os.unlink(path)


# ─────────────────────────────────────────────────────────────────────────────
# Normalizer
# ─────────────────────────────────────────────────────────────────────────────
class TestNormalizer:
    def test_output_is_list(self, minimal_variants):
        result = normalize_variants(minimal_variants)
        assert isinstance(result, list)

    def test_all_have_rsid(self, minimal_variants):
        result = normalize_variants(minimal_variants)
        for v in result:
            assert "rsid" in v and v["rsid"]

    def test_all_have_genotype(self, minimal_variants):
        result = normalize_variants(minimal_variants)
        for v in result:
            assert "genotype" in v

    def test_strips_invalid_rsids(self):
        bad = [{"rsid": "rs--", "chromosome": "1", "position": 123, "genotype": "AA"}]
        result = normalize_variants(bad)
        assert len(result) == 0

    def test_deduplicates_rsids(self):
        dupes = [
            {"rsid": "rs4244285", "chromosome": "10", "position": 96541616, "genotype": "AG"},
            {"rsid": "rs4244285", "chromosome": "10", "position": 96541616, "genotype": "AG"},
        ]
        result = normalize_variants(dupes)
        rsids = [v["rsid"] for v in result]
        assert len(rsids) == len(set(rsids))

    def test_empty_input_returns_empty(self):
        assert normalize_variants([]) == []
