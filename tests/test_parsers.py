# ── Helpers ───────────────────────────────────────────────────────────────────
def _write_tmp(content: str, suffix: str) -> str:
    """Write a string to a temp file and return the path."""
    f = tempfile.NamedTemporaryFile(mode="w", suffix=suffix, delete=False)
    f.write(textwrap.dedent(content))
    f.flush()
    f.close()
    return f.name


# ─────────────────────────────────────────────────────────────────────────────
# 23andMe parser
# ─────────────────────────────────────────────────────────────────────────────
TWENTYTHREEME_CONTENT = """\
    # This data file generated by 23andMe at: Mon Jun 22 2026
    # This file contains raw genotype data, including data that is not used in 23andMe reports.
    # rsid\tchromosome\tposition\tgenotype
    rs4244285\t10\t96541616\tAG
    rs7903146\t10\t114758349\tCT
    rs12913832\t15\t28365618\tAG
    rs429358\t19\t44908684\tCT
    rs--\t1\t12345\tAA
"""


class TestTwentyThreeMeParser:
    def setup_method(self):
        self.path = _write_tmp(TWENTYTHREEME_CONTENT, ".txt")

    def teardown_method(self):
        os.unlink(self.path)

    def test_returns_list(self):
        variants = parse_23andme(self.path)
        assert isinstance(variants, list)

    def test_correct_count(self):
        variants = parse_23andme(self.path)
        # 5 data rows; rs-- is an invalid rsid and should be skipped
        assert len(variants) == 4

    def test_fields_present(self):
        v = parse_23andme(self.path)[0]
        assert "rsid"       in v
        assert "chromosome" in v
        assert "position"   in v
        assert "genotype"   in v

    def test_rsid_format(self):
        variants = parse_23andme(self.path)
        for v in variants:
            assert v["rsid"].startswith("rs")
            assert v["rsid"][2:].isdigit()

    def test_genotype_uppercase(self):
        variants = parse_23andme(self.path)
        for v in variants:
            assert v["genotype"] == v["genotype"].upper()

    def test_position_is_int(self):
        variants = parse_23andme(self.path)
        for v in variants:
            assert isinstance(v["position"], int)

    def test_comments_ignored(self):
        variants = parse_23andme(self.path)
        rsids = [v["rsid"] for v in variants]
        assert not any(r.startswith("#") for r in rsids)


